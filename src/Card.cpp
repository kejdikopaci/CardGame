//------------------------------------------------------------------------------
// Card.cpp
//------------------------------------------------------------------------------

#include "Card.hpp"

using Oop::Card;

//------------------------------------------------------------------------------
Card::Card() : name_(""),
               mana_cost_(0),
               type_(static_cast<CardType >(0xff))
{
}

//------------------------------------------------------------------------------
Card::Card(std::string name,
           int mana_cost,
           Oop::Card::CardType type) :
  name_(name),
  mana_cost_(mana_cost),
  type_(type)
{
}

//------------------------------------------------------------------------------
Card::~Card()
{
}

//------------------------------------------------------------------------------
std::string Card::getName() const
{
  return name_;
}

//------------------------------------------------------------------------------
Card::CardType Card::getType() const
{
  return type_;
}

//------------------------------------------------------------------------------
int Card::getManaCost() const
{
  return mana_cost_;
}
